import UIKit

internal extension UIColor {
    
    convenience init(red: Int, green: Int, blue: Int) {
        let invFF: CGFloat = 1.0/255.0
        self.init(red: CGFloat(red) * invFF, green: CGFloat(green) * invFF, blue: CGFloat(blue) * invFF, alpha: 1.0)
    }
    
    convenience init(rgb: Int) {
        self.init(
            red: (rgb >> 16) & 0xFF,
            green: (rgb >> 8) & 0xFF,
            blue: rgb & 0xFF
        )
    }
}
